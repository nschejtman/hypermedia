                <div role="tabpanel" class="tab-pane" id="adsl-and-fiber-tab">
                    <h2>ADSL and Fiber</h2>
                    <hr>
                    <h3 class="gray">Cannot surf the web</h3>
                    <p class="text-justify">
                        If you can not browse, check on your modem, the LEDs Power, ADSL / FIBER and Service are on and
                        green.<br><br>

                        If the LEDs do not light up or blink you'll want to:
                    </p>
                    <ul>
                        <li>restart your modem</li>
                        <li>replug all the connections to the electrical outlet and phone jack</li>
                        <li>connect a PC or tablet to the modem and wait until the Internet LED turns green.</li>
                    </ul>
                    <p class="text-justify">
                        If, after re-connected devices (mobile, PC or tablet, etc.), the LEDs are off or flashing, or,
                        if the LEDs are lit in green, but you can not surf, plase get in touch with tech support at line
                        187.
                    </p>
                    <hr>
                    <h3 class="gray">The connection is very slow</h3>
                    <p class="text-justify">
                        To navigate better with your line of TIM Fiber and optimize the connection speed, we recommend
                        you to verify your telephone and the internal network system.<br><br>

                        Follow these steps:
                    </p>
                    <ul>
                        <li>Disconnect all phones and all the eventual ADSL filters all of the telephone jacks and
                            connectthe phones directly to the modem (red doors at the back).
                        </li>
                        <li>Connect the modem to the main fiber intake without using the filter.</li>
                        <li>The presence of telephone extensions or an excessive number of derived plugs into your
                            system could help to deteriorate the fiber signal and reduce the speed.<br>
                            Remove them and disconnect all telephone derived plugs on your system.
                        </li>
                    </ul>
                    <p class="text-justify">
                        If you do not have your own trusted technician and you need help to configure your telephone
                        system, TIM Fixed Line offers you the after sales service SOS paid PC to request an
                        administrative attention at home.<br><br>

                        After finishing on the system checks, before performing a speed test:
                    </p>
                    <ul>
                        <li>
                            Connect your PC using an Ethernet cable. If possible use devices with Ethernet ports to
                            1Gigabyte ****
                        </li>
                        <li>
                            If you use the wi-fi network, remember that the speed can be influenced by several factors,
                            so
                            be sure to:
                            <ul>
                                <li>place the PC close to the modem</li>
                                <li>use the 5Ghz WiFi network present on the latest devices</li>
                                <li>check that there are no other devices in your network that are simultaneously
                                    performing other traffic (file-sharing programs, peer to peer, etc).
                                </li>
                                <li>
                                    temporarily disconnect other devices from the modem
                                </li>
                            </ul>
                        </li>
                    </ul>
                    <p class="text-justify">
                        **** If you have a contract that provides higher speed 100MEGA, remember that you can take full
                        advantage of the speed only if you connect your Ethernet device using a network card with
                        1Gigabyte speed.<br><br>

                        If you are browsing from a PC, you can immediately measure the connection speed through a
                        speed test.
                    </p>
                    <hr>
                    <h3 class="gray">The connection frequently disconnects</h3>
                    <p class="text-justify">
                        The frequent connection drops can be caused by different factors such as poor reception of the
                        Wi-Fi signal or incorrect or improper connections on telephone system.<br><br>

                        If you use the connection through the Wi-Fi network you will want to run some simple steps:
                    </p>
                    <ul>
                        <li>verify the reception of the Wi-Fi signal and, if not optimal, change Wi-Fi channel. You can
                            do this automatically via the app MyTIM Fisso or go to the web page of the modem
                        </li>
                        <li>
                            Place the modem in central point with respect to the local (home or office)
                        </li>
                    </ul>

                    <p class="text-justify">
                        If you see frequent disconnections ADSL / fiber signal and the corresponding LED blinks or is
                        off:
                    </p>
                    <ul>
                        <li>Check the connections to the modem</li>
                        <li> Disconnect all phones and filters connected to all telephone sockets in your system and
                            connect the modem directly to the mains socket, without using the filter
                        </li>
                        <li>
                            Check that in your telephone system there are no extensions, remote alarm systems and remote
                            assistance systems
                        </li>
                    </ul>
                    <p class="text-justify">
                        We inform you that our service is available for a fee PC SOS Service: technical staff will
                        arrange a personalized intervention to optimally configure both your Wi-Fi network that your
                        facility.
